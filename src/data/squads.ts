import type { Player, Position, Squad } from './types';

// ---------------------------------------------------------------------------
// WORLD CUP SQUAD DATASET.
//
// 1998 / 2002 / 2006: hand-authored PLACEHOLDER squads (approximate names,
// numbers, positions and elo ratings from memory - expect errors).
//
// 2022 (Qatar): full researched dataset - all 32 nations, each with its
// official 26-man squad (Iran registered 25), shirt numbers and positions
// verified against the FIFA / Wikipedia tournament squad lists. Elo ratings
// are a holistic judgement of each player's STRENGTH AT THE TIME of the
// tournament (Nov/Dec 2022, NOT current), cross-checked and debated across
// several research passes. Scale: 60 (weakest) .. 99 (best); duplicates are
// expected and fine.
//
// Each squad is a single nation at a single tournament. Players are tagged with
// one or more specific positions (a slot only accepts a matching position).
// Central midfielders carry their real role(s): DM (holding), CM (central),
// AM (attacking) - often a combo such as DM/CM or AM/CM.
//
// `personId` is derived from the name, so the same human appearing in multiple
// squads (e.g. Messi in Argentina 2006 and 2022) shares one identity and can
// only be drafted once. Keep name spellings identical across squads for that to
// work; distinct people MUST have distinct names (e.g. Amadou Onana vs Andre
// Onana, Marcus Thuram vs Thuram/Lilian) or they would collide into one person.
// ---------------------------------------------------------------------------

function slug(name: string): string {
    // Strip combining diacritics (NFD splits 'í' into 'i' + accent, U+0300..U+036F).
    return name
        .toLowerCase()
        .normalize('NFD')
        .replace(/[̀-ͯ]/g, '')
        .replace(/[^a-z0-9]+/g, '-')
        .replace(/^-+|-+$/g, '');
}

type Row = [number, string, Position[], number];

function squad(code: string, nation: string, year: number, rows: Row[]): Squad {
    const id = `${code.toLowerCase()}-${year}`;
    const players: Player[] = rows.map(([number, name, positions, elo]) => ({
        id: `${id}-${number}`,
        personId: slug(name),
        squadId: id,
        number,
        name,
        positions,
        elo,
    }));
    return { id, code, nation, year, players };
}

